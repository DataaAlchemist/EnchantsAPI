mongoose = require('mongoose');
const Schema = mongoose.Schema;

const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        unique: true
    }
}, 
{ timestamps: true });

User = mongoose.model('user', UserSchema);
module.exports = User;